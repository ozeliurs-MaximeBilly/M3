CREATE OR REPLACE TRIGGER NOTE
BEFORE INSERT UPDATE
ON NOTATION
BEGIN
    FOR P IN (SELECT NOTEMAX FROM CONTROLE WHERE NEW.IDC = CONTROLE.IDC)
    LOOP
        NEW.NOTEDEF = NEW.NOTE/P.NOTEMAX*20;
    END LOOP;
END;

