CREATE OR REPLACE TRIGGER EMPRT
BEFORE INSERT
ON EMPRUNT
BEGIN
    FOR P IN (SELECT COUNT(*) TOTAL FROM EMPRUNT WHERE DATERET IS NULLAND NEW.IDA = EMPRUNT.IDA)
    LOOP
        IF P.TOTAL > 5 THEN
            NEW = NULL;
        END IF;
    END LOOP;
END;

