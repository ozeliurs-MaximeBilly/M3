CREATE OR REPLACE TRIGGER NBEMPRT
BEFORE INSERT
ON EMPRUNT
BEGIN
    FOR P IN (SELECT COUNT(*) TOTAL, EMPRUNT FROM EMPRUNT WHERE DATERET IS NULL AND NEW.IDA = EMPRUNT.IDA)
    LOOP
        UPDATE ABONNE SET NBEMPRUNT = P.TOTAL WHERE NEW.IDA = EMPRUNT.IDA;
    END LOOP;
END;